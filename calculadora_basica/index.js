// FUNÇÕES PARA CADA OPERAÇÃO MATEMÁTICA

function adicao(a, b) {
    return a + b
}
function subtra(a, b) {
    return a - b
}
function div(a, b) {
    return a / b
}
function multi(a, b) {
    return a * b
}

console.log(adicao(2, 10))
console.log(subtra(10, 2))
console.log(div(10, 2))
console.log(multi(2, 10))