// FUNÇÕES PARA CADA OPERAÇÃO MATEMÁTICA
const calculadora = {
    somarNum: function (a, b) {
        return a + b
    },
    subtrairNums: function (a, b) {
        return a - b
    },
    dividirNums: function (a, b) {
        return a / b
    },
    multiplicarNums: function (a, b) {
        return a * b
    }
}

module.exports = calculadora